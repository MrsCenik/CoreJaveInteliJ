package day03increment_decrement_operators;

public class OrderOfOperations01 {

	public static void main(String[] args) {
		/*
		 Order if opertation
		 1- Calculate Exponents 
		 2- ()
		 3- Do multiplication and division from left to right
		 4- Do addition and subtruction from left to right
		 
		 */
		
		// 38 / 2 - (4 + 3) * 2 = ?
		
		System.out.println(38 / 2 - (4 + 3) * 2);
		
		// 8+2*(14-6/2)-12=?
		System.out.println(8+2*(14-6/2)-12);
		
		
	}

}
